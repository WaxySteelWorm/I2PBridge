# Privacy Policy for I2P Bridge

**Last Updated:** July 20, 2025

This Privacy Policy describes how I2P Bridge ("we," "us," or "our") handles information in connection with the I2P Bridge mobile application (the "App").

### Our Privacy Philosophy

I2P Bridge is designed from the ground up with your privacy as its highest priority. Our goal is to provide access to the I2P network without compromising your anonymity. We operate on a "zero-knowledge" principle, meaning we, the operators of the service, have no ability to see, log, or store your activity.

### Information We Do Not Collect

We do not collect, log, store, or share any of the following information:

* Your IP address.
* The I2P sites you visit.
* The content of your IRC messages, emails, or any other data you transmit.
* Any personal information that could be used to identify you.

The bridge server operated by us acts solely as a secure relay to forward your encrypted requests to the I2P network.

### Information Stored Locally on Your Device

The App stores certain information locally on your device for your convenience. This data is under your control and is never transmitted to us or any third party. This includes:

* **App Settings:** Such as your default IRC nickname and any saved passwords for services like NickServ. This information is stored securely on your device's local storage.
* **Browser History & Cache:** To improve performance, the app may cache content from sites you visit and maintain a local browsing history. This data remains on your device.

### How Your Data is Handled

When you use the App, your requests are encrypted on your device and sent to our bridge server. The server forwards this encrypted data to the I2P network without decrypting it. Only the final destination on the I2P network can decrypt your request.

### Third-Party Services

The App's sole purpose is to connect to the I2P network, which is an anonymous, decentralized, and volunteer-run network. We do not control the services you access on the I2P network, and they are not covered by this Privacy Policy.

### Changes to This Privacy Policy

We may update this Privacy Policy from time to time. We will notify you of any changes by posting the new Privacy Policy within the App.

### Contact Us

If you have any questions about this Privacy Policy, please contact us.
